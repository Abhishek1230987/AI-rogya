import os

# Create a minimal WebM file
header = bytes([
    0x1a, 0x45, 0xdf, 0xa3,  # EBML ID
    0xa3,  # Element size
    0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
    0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
    0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x04,
    0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x08,
])

# Add padding
data = header + (b'\x00' * (2048 - len(header)))

with open('test-audio.webm', 'wb') as f:
    f.write(data)

print(f'Test file created: {os.path.getsize("test-audio.webm")} bytes')
